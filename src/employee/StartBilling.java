    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if(b)
        {
            String quantity = (String)jComboBox1.getSelectedItem();
            int quantityy = Integer.parseInt(quantity.replace(",", ""));
            String item_price = jTextField3.getText();
            int item_pricee = Integer.parseInt(item_price.replace(",", "").trim());

            int total_price = quantityy*item_pricee;
            jTextField5.setText(String.valueOf(total_price));
            b = false;
        }
        else{
            b = true;
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      String item_id = jTextField1.getText();
      String item_name = jTextField2.getText();
      String item_price = jTextField3.getText();
      String quantity = (String)jComboBox1.getSelectedItem();
      
        al = new ArrayList<>();
       
        al.add(item_name);
        al.add(item_price);
        al.add(quantity);
        hm.put(item_id, al);
        JOptionPane.showMessageDialog(rootPane, "Item added Successfully");
        jTextField1.setText("");
        emptyDetails();
        jButton2.setEnabled(true);
        
    }//GEN-LAST:event_jButton1ActionPerformed

